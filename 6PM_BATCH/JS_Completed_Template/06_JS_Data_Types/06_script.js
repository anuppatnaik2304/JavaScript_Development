// number example
var a = 10;
console.log('Value : ' + a + ' DataType : ' + typeof a);

// String Examples
var language = 'JavaScript';
console.log('Value : ' + language + ' Data Type : ' + typeof language);

// boolean Examples
var isJSEasy = true;
console.log('Value : ' + isJSEasy + ' Data Type : ' + typeof isJSEasy);

// reassignment example of variables
var abc;
console.log('Value : ' + abc + ' Data Type : ' + typeof abc);

abc = 100;
console.log('Value : ' + abc + ' Data Type : ' + typeof abc);

abc = 'test';
console.log('Value : ' + abc + ' Data Type : ' + typeof abc);

abc = true;
console.log('Value : ' + abc + ' Data Type : ' + typeof abc);

abc = null;
console.log('Value : ' + abc + ' Data Type : ' + typeof abc);